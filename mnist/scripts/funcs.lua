-- Split function, copied from http://stackoverflow.com/questions/1426954/split-string-in-lua (accessed 2015-04-27)

function split(inputstr, sep)
	if sep == nil then
		sep = "%s"
	end
	local t={} ; i=1
	for str in string.gmatch(inputstr, "([^"..sep.."]+)") do
		t[i] = str
		i = i + 1
	end
	return t
end
